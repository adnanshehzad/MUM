package lesson3.labs.prob2;

public class Apartment {
	private int numb;
	private double rent;
	public Apartment(int numb, double rent) {
		super();
		this.numb = numb;
		this.rent = rent;
	}
	public int getNumb() {
		return numb;
	}
	
	public double getRent() {
		return rent;
	}

}
